package main

import "fmt"

func main() {
	// Short declaration operator ":="
	x := 14 // declares variable x and assign value
	fmt.Println(x)
	x = 1989 // new value assigned "=" sign only
	fmt.Println(x)

	y := 90 + 10 // variables must be used
	// + is the operator
	// 90 and 10 are the operands
	fmt.Println(y)
}
